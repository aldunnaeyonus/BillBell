// Helper to normalize vendor names (for fuzzy domain matching)
export const normalizeVendorKey = (input: string) =>
  (input || "")
    .toLowerCase()
    .replace(/&/g, "and")
    .replace(/\+/g, "plus")
    .replace(/[^a-z0-9]+/g, "")
    .trim();

